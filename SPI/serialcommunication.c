extern void serialcom_printchar(INT8U inpchar)
/*****************************************************************************
*   Input    : -
*   Output   : -
*   Function : Test function
******************************************************************************/
{
    while(!uart0_tx_rdy());

    uart0_putc(inpchar);
}

extern INT8U serialcom_getchar(void)
/*****************************************************************************
*   Input    : -
*   Output   : -
*   Function : Test function
******************************************************************************/
{
    if(uart0_rx_rdy())
    {
        return uart0_getc();
    }
    else
    {
        return 0;
    }
}

extern void serialcom_printnl(void)
/*****************************************************************************
*   Input    : -
*   Output   : -
*   Function : Test function
******************************************************************************/
{
    serialcom_printchar(0x0a);
    serialcom_printchar(0x0d);
}

